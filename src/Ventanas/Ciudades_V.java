    private void Count () {
    
        try {
            
            Ciudades_C a = new Ciudades_C();
            jTextField1.setText(a.New());
            
        } catch (Exception e) {
            
            this.tfciudad.requestFocus();
            
        }
    
    }
    
    private void update () {
        
        int tam = tciudad.getRowCount();
        
        for (int i = 0; i < tam; i++) {
            
            dtmciudades.removeRow(0);
            
        }
        
    }

    private void star () {
        
        dtmciudades = (DefaultTableModel) tciudad.getModel();
        Ciudades_C c = new Ciudades_C();
        ArrayList<Ciudades_C> all = c.SearchAll();
        
        for(int i=0; i < all.size(); i++ ){
            
            dtmciudades.addRow(new Object[]{all.get(i).getId(), all.get(i).getName()});
            
        }
        
        this.jTextField1.setText("");
        
    }
    
    public static Integer getInt(String cadena){
        return ( (cadena == null || cadena.isEmpty() ) ? 0 : Integer.parseInt(cadena.trim()));
    }
    
    public static String getString(String cadena){
        return (cadena == null ? "" : cadena);
    }
    
