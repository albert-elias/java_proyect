    private void bguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bguardarActionPerformed
        
        this.Count();
        number = getInt(this.jTextField1.getText());
        name = tfciudad.getText();
        
        if (name.equals("")) {
            
            JOptionPane.showMessageDialog(rootPane, "Campo Vacio. \n Registro anulado.");
            
        } else {
        
            Ciudades_C a = new Ciudades_C(number, name);
            Ciudades_C already = a.Search(String.valueOf(number));
            
            if (already == null) {
                
                if (a.Insert()) {
                    
                    JOptionPane.showMessageDialog(rootPane, "Registro exitoso!");
                    
                } else {
                
                    JOptionPane.showMessageDialog(rootPane, "Ya existe: "+name+"");
                    
                    
                }
                
                dtmciudades = (DefaultTableModel) tciudad.getModel();
                int rows = tciudad.getRowCount();
                for (int i = 0; rows < i; i++) {
                    
                    dtmciudades.removeRow(0);
                    
                }
                
                this.update();
                this.star();
                this.tfciudad.setText("");
                
            }
        
        }
        
    }//GEN-LAST:event_bguardarActionPerformed

    private void bdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeleteActionPerformed
        
        String code = jTextField1.getText();
        Ciudades_C ciu = new Ciudades_C();
        
        if(ciu.Delete(code)){
            
            JOptionPane.showMessageDialog(this, "Eliminando satisfactoriamente");
            
        }else{
            
            JOptionPane.showMessageDialog(this, "Imposible eliminar");
            
        }
        
        this.update();
        this.star();
        
    }//GEN-LAST:event_bdeleteActionPerformed

