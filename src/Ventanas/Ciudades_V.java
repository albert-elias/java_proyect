    private void tciudadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tciudadMouseClicked
        
        this.bdelete.setEnabled(true);
        this.bguardar.setEnabled(true);
        dtmciudades = (DefaultTableModel) this.tciudad.getModel();
        int selRow = this.tciudad.getSelectedRow();
        Ciudades_C ciu = new Ciudades_C();
        
        if(selRow != -1){
            
            String id = String.valueOf(dtmciudades.getValueAt(selRow, 0).toString());
            Ciudades_C finded = ciu.Search(id);
            
            if(!(finded == null)){
                
                this.jTextField1.setText(String.valueOf(finded.getId()));
                
            }
            
        }
        
    }//GEN-LAST:event_tciudadMouseClicked

