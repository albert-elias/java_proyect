    private void jtmodopagoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtmodopagoMouseClicked
        
        this.jbdelete.setEnabled(true);
        this.jbupdate.setEnabled(true);
        dtm = (DefaultTableModel) this.jtmodopago.getModel();
        int selRow = this.jtmodopago.getSelectedRow();
        ModoPago_C car = new ModoPago_C();
        
        if(selRow != -1){
            
            String id = String.valueOf(dtm.getValueAt(selRow, 0).toString());
            ModoPago_C finded = car.Search(id);
            
            if(!(finded == null)){
                
                this.jTextField1.setText(String.valueOf(finded.getId()));
                
            }
            
        }
        
    }//GEN-LAST:event_jtmodopagoMouseClicked

