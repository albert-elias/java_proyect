    private void jbnewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnewActionPerformed
        
        this.Count();
        id = getInt(this.jTextField1.getText());
        name = JOptionPane.showInputDialog("Nuevo cargo: ");
        
        if (name.equals("")) {
            
            JOptionPane.showMessageDialog(rootPane, "Campo Vacio. \n Registro anulado.");
            
        } else {
        
            ModoPago_C a = new ModoPago_C(id, name);
            ModoPago_C already = a.Search(String.valueOf(id));
            
            if (already == null) {
                
                if (a.Insert()) {
                    
                    JOptionPane.showMessageDialog(rootPane, "Registro exitoso!");
                    
                } else {
                
                    JOptionPane.showMessageDialog(rootPane, "Ya existe: "+name+"");
                    
                    
                }
                
                dtm = (DefaultTableModel) jtmodopago.getModel();
                int rows = jtmodopago.getRowCount();
                for (int i = 0; rows < i; i++) {
                    
                    dtm.removeRow(0);
                    
                }
                
                this.update();
                this.star();
                
            }
        
        }
        
    }//GEN-LAST:event_jbnewActionPerformed

    private void jbupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbupdateActionPerformed
        
        id = getInt(this.jTextField1.getText());
        name = JOptionPane.showInputDialog("Nuevo nombre: ");
        ModoPago_C upd = new ModoPago_C(id, name);
        
        if(upd.Update()){
            
            JOptionPane.showMessageDialog(rootPane, "Registro modficado");
            this.update();
            this.star();
            
        }else{
            
            JOptionPane.showMessageDialog(rootPane, "No modificado");
            
        }
        
    }//GEN-LAST:event_jbupdateActionPerformed

    private void jbdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbdeleteActionPerformed
        
        String code = jTextField1.getText();
        ModoPago_C mp = new ModoPago_C();
        
        if(mp.Delete(code)){
            
            JOptionPane.showMessageDialog(this, "Eliminando satisfactoriamente");
            
        }else{
            
            JOptionPane.showMessageDialog(this, "Imposible eliminar");
            
        }
        
        this.update();
        this.star();
        
    }//GEN-LAST:event_jbdeleteActionPerformed

