    private void update () {
        
        int tam = jtmodopago.getRowCount();
        
        for (int i = 0; i < tam; i++) {
            
            dtm.removeRow(0);
            
        }
        
    }
    
    private void star () {
        
        dtm = (DefaultTableModel) jtmodopago.getModel();
        ModoPago_C c = new ModoPago_C();
        ArrayList<ModoPago_C> all = c.SearchAll();
        
        for(int i=0; i < all.size(); i++ ){
            
            dtm.addRow(new Object[]{all.get(i).getId(), all.get(i).getName()});
            
        }
        
        this.jTextField1.setText("");
        
    }
    
