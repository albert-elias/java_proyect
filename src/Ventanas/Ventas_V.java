    private void update () {
        
        int tam = tsale.getRowCount();
        
        for (int i = 0; i < tam; i++) {
            
            dtm.removeRow(0);
            
        }
        
    }
    
    private void star () {
        
        dtm = (DefaultTableModel) tsale.getModel();
        ArrayList<vista_ventas> all = vv.buscarventa();
        
        for(int i = 0; i < all.size(); i++ ){
            
            dtm.addRow(new Object[]{all.get(i).getCodigo(), all.get(i).getFactura(),
            all.get(i).getCliente(), all.get(i).getMonto(), all.get(i).getVendedor(),
            all.get(i).getFecha()});
            
        }
        
    }
    
    public void search (String search) {
        
        String options = (String)cbsearch.getSelectedItem();
        dtm = (DefaultTableModel) tsale.getModel();
        ArrayList<vista_ventas> list = null;
        
        switch (options) {
            case "Codigo":
                list = vv.SearchCodigo(search);                
                break;

            case "Cliente":
                list = vv.SearchCliente(search);
                break;

            case "Vendedor":
                list = vv.SearchVendedor(search);
                break;

        }
        
        if (list != null) {

            update();

            for(int i=0; i < list.size(); i++ ){
            
                dtm.addRow(new Object[]{list.get(i).getCodigo(), list.get(i).getFactura(),
                list.get(i).getCliente(), list.get(i).getMonto(), list.get(i).getVendedor(),
                list.get(i).getFecha()});

            }

        }
    
    }
    
