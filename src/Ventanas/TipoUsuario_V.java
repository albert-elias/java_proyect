    private void ttipousuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ttipousuarioMouseClicked
        
        this.bdelete.setEnabled(true);
        t_tipousuario = (DefaultTableModel) this.ttipousuario.getModel();
        int selRow = this.ttipousuario.getSelectedRow();
        
        if(selRow != -1){
            
            String id = String.valueOf(t_tipousuario.getValueAt(selRow, 0).toString());
            TipoUsuario_C finded = tu.Search(id);
            
            if(!(finded == null)){
                
                this.jTextField1.setText(String.valueOf(finded.getId()));
                
            }
            
        }
        
    }//GEN-LAST:event_ttipousuarioMouseClicked

