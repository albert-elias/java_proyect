    private void bnewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnewActionPerformed
        
        this.Count();
        number = getInt(this.jTextField1.getText());
        name = JOptionPane.showInputDialog("Nuevo usuario: ");

        if (name == null) {
            
            int response = getInt(name);
            
            if (response == JOptionPane.CANCEL_OPTION) {
                
                JOptionPane.showMessageDialog(rootPane, "Cancelado");
                
            }
            
            return;
            
        }
        
        if (name.equals(null)) {
        
            JOptionPane.showMessageDialog(rootPane, "Campo vacio");
            
        } else {
        
            TipoUsuario_C a = new TipoUsuario_C(number, name);
            TipoUsuario_C already = a.Search(String.valueOf(number));
            
            if (already == null) {
                
                if (a.Insert()) {
                    
                    JOptionPane.showMessageDialog(rootPane, "Registro exitoso!");
                    
                } else {
                
                    JOptionPane.showMessageDialog(rootPane, "Ya existe: "+name+"");
                    
                    
                }
                
                t_tipousuario = (DefaultTableModel) ttipousuario.getModel();
                int rows = ttipousuario.getRowCount();
                for (int i = 0; rows < i; i++) {
                    
                    t_tipousuario.removeRow(0);
                    
                }
                
                this.update();
                this.star();
                
            }
        
        }
        
    }//GEN-LAST:event_bnewActionPerformed

    private void bdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeleteActionPerformed
        
        String code = jTextField1.getText();
        
        if(tu.Delete(code)){
            
            JOptionPane.showMessageDialog(this, "Eliminando satisfactoriamente");
            
        }else{
            
            JOptionPane.showMessageDialog(this, "Imposible eliminar");
            
        }
        
        this.update();
        this.star();
        
    }//GEN-LAST:event_bdeleteActionPerformed

