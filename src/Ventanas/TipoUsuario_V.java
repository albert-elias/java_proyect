    private void update () {
        
        int tam = ttipousuario.getRowCount();
        
        for (int i = 0; i < tam; i++) {
            
            t_tipousuario.removeRow(0);
            
        }
        
    }

    private void star () {
        
        t_tipousuario = (DefaultTableModel) ttipousuario.getModel();
        ArrayList<TipoUsuario_C> all = tu.SearchAll();
        
        for(int i=0; i < all.size(); i++ ){
            
            t_tipousuario.addRow(new Object[]{all.get(i).getId(), all.get(i).getName()});
            
        }
        
        this.jTextField1.setText("");
        
    }
    
