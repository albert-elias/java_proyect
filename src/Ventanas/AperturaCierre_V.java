    private void topencloseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_topencloseMouseClicked
        
        try {
            
            dtm = (DefaultTableModel) this.topenclose.getModel();
            int selRow = this.topenclose.getSelectedRow();
            this.bupdate.setEnabled(true);
            this.bsave.setEnabled(false);

            if(selRow != -1){

                String id = String.valueOf(dtm.getValueAt(selRow, 0).toString());
                AperturaCierre_C finded = ac.Search(id);

                if(!(finded == null)){

                    this.tfcode.setText(String.valueOf(finded.getId()));
                    this.tfcode.setEditable(false);
                    this.tfdate.setText(finded.getDate());
                    this.tfdate.setEditable(false);
                    this.tfcollected.setText(finded.getCollect());
                    this.tfcollected.setEditable(false);
                    this.tfjob.setText(String.valueOf(finded.getFun()));
                    this.tfjob.setEditable(false);

                 }

            }
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, "Error al encontrar archivo"+e);
            
        }
        
    }//GEN-LAST:event_topencloseMouseClicked

