    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        
        Count();
        id = getInt(tfcode.getText());
        fecha = tfdate.getText();
        total = tfcollected.getText();
        fun = Integer.parseInt(tfjob.getText());
        
        if ((id <= 0) && (fecha.isEmpty()) && (fun <=0)) {
            
            JOptionPane.showMessageDialog(rootPane, "Complete los campos");
            
        } else {
        
            AperturaCierre_C ap = new AperturaCierre_C(id, fecha, total, fun);
            AperturaCierre_C already = ap.Search(String.valueOf(id));
            
            if (already == null) {
                
                if (ap.Insert()) {
                    
                    JOptionPane.showMessageDialog(rootPane, "Registro guardado con exito");
                    
                } else {
                
                    JOptionPane.showMessageDialog(rootPane, "El codigo u otro dato ya existe");
                
                }
                
                dtm = (DefaultTableModel) topenclose.getModel();
                int rows = topenclose.getRowCount();
                
                for (int i = 0; rows < i; i++) {
                    
                    dtm.removeRow(0);
                    
                }
                
                this.update();
                this.star();
                this.clean();
                
            }
            
        
        }
        
    }//GEN-LAST:event_bsaveActionPerformed

