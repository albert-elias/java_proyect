    private void clean () {
    
        tfcode.setText("");
        tfcollected.setText("");
        tfjob.setText("");
    
    }
    
    private void update () {
        
        int tam = topenclose.getRowCount();
        
        for (int i = 0; i < tam; i++) {
            
            dtm.removeRow(0);
            
        }
        
    }

    private void star () {
        
        dtm = (DefaultTableModel) topenclose.getModel();
        ArrayList<AperturaCierre_C> all = ac.SearchAll();
        
        for(int i=0; i < all.size(); i++ ){
            
            dtm.addRow(new Object[]{all.get(i).getId(), all.get(i).getDate(), all.get(i).getCollect(), all.get(i).getFun()});
            
        }
        
        this.tfdate.setText(AperturaCierre_C.fechaActual());
        
    }
    
